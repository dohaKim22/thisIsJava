package Java_04;

import java.util.Scanner;

public class Mul {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        System.out.println("정수를 입력 : ");
        int a = scanner.nextInt();
        int b = scanner.nextInt();

        int result;
        result = a * b;
        System.out.println(result);



    }
}
